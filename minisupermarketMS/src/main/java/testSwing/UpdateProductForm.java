    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        String newName = null;
        newName = txtNewNAmeTab2.getText();
        if (!newName.equals(product.getName())) {
            if (newName == null || newName.equals("")) {
                JOptionPane.showMessageDialog(this, "Tên sản phẩm không được bỏ trống", "Kiểm tra tên",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            boolean checkExist = new ProductBL().getByName(newName);
            if (checkExist) {
                JOptionPane.showMessageDialog(this, "Tên cho sản phẩm này đã tồn tại trong hệ thống");

                return;
            }
        }
        // check quantity
        Integer quantity = product.getLeftQuantity();
        try {
            quantity = (Integer) spUpdateQuantity.getValue();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi dữ liệu ở mục \"Số lượng\"", "Lỗi Dữ Liệu",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        String description = txtAreaDescription.getText();
        System.out.println(description);
        Double price = product.getPrice();
        try {
            price = (Double) spUpdatePrice.getValue();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi dữ liệu ở mục \"Giá Bán\"", "Lỗi Dữ Liệu",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (price==0){
            if (price==0) {
                JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Giá Bán", "Nhập Giá Bán", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        int selected=JOptionPane.showConfirmDialog(this, "Cập nhật sản phẩm ?");
        if (selected==JOptionPane.NO_OPTION||selected==JOptionPane.CANCEL_OPTION) {
            return;
        }
        if (newName!=product.getName()) {
            product.setName(newName);
        }
        if (!description.equals(product.getDescription())){
            product.setDescription(description);
        }
        if (quantity!=product.getLeftQuantity()) {
            product.setLeftQuantity(quantity);
        }
        if (price!=product.getPrice()) {
            product.setPrice(price);
        }
        if (new ProductBL().updateProduct(product)) {
            JOptionPane.showMessageDialog(this, "Cập Nhật Thành Công");
            this.dispose();
        }
    }

