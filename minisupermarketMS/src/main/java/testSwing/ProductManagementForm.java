    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:\
        // checking newProductId text field
        String newId = null;
        newId = txtNewProductId.getText();

        if (newId == null || newId.equals("")) {
            JOptionPane.showMessageDialog(this, "Mã sản phẩm không được bỏ trống!", "Kiểm tra mã",
                    JOptionPane.ERROR_MESSAGE);
            lbForCheckingExistNewProductId.setText("");
            return;
        }
        Product product = new ProductBL().getById(newId);
        if (product != null) {
            JOptionPane.showMessageDialog(this, "Mã sản phẩm này đã tồn tại trong hệ thống");
            lbForCheckingNewName.setText("Mã đã tồn tại");
            lbForCheckingNewName.setForeground(Color.red);
            return;
        } else {
            lbForCheckingNewName.setText("Hợp lệ");
            lbForCheckingNewName.setForeground(Color.black);
        }
        // checking newNameTextField
        String newName = null;
        newName = txtNewName.getText();

        if (newName == null || newName.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên sản phẩm không được bỏ trống", "Kiểm tra tên",
                    JOptionPane.ERROR_MESSAGE);
            lbForCheckingNewName.setText("");
            return;
        }
        boolean checkExist = new ProductBL().getByName(newName);
        if (checkExist) {
            JOptionPane.showMessageDialog(this, "Tên cho sản phẩm này đã tồn tại trong hệ thống");
            lbForCheckingExistNewProductId.setText("Tên đã tồn tại");
            lbForCheckingExistNewProductId.setForeground(Color.red);
            return;
        } else {
            lbForCheckingExistNewProductId.setText("Hợp lệ");
            lbForCheckingExistNewProductId.setForeground(Color.black);
        }
        // checking quantity Spinner
        Integer quantity = 0;
        try {
            quantity = (Integer) spNewQuantity.getValue();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi dữ liệu ở mục \"Số lượng\"", "Lỗi Dữ Liệu",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (quantity == 0) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Số Lượng", "Nhập số lượng", JOptionPane.ERROR_MESSAGE);
            return;
        }
        // checking price Spinner
        Double price = 0.;
        try {
            price = (Double) spNewPrice.getValue();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi dữ liệu ở mục \"Giá Bán\"", "Lỗi Dữ Liệu",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (price == 0) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Giá Bán", "Nhập Giá Bán", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String description=txtDescription.getText();
        try {
            Product product1 = new Product();
            product1.setProductId(newId);
            product1.setName(newName);
            product1.setDescription(description);
            product1.setPrice(price);
            product1.setCalculationUnit("Lẻ");
            product1.setLeftQuantity(quantity);
            if (productBL.insertProduct(product1)) {
                JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công!");
                txtNewProductId.setText("");
                txtNewName.setText("");
                txtDescription.setText("");
                spNewPrice.setValue(0);
                spNewQuantity.setValue(0);
            } else {
                JOptionPane.showMessageDialog(this, "Đã có lỗi xảy ra, vui lòng thử lại sau....", "Oops",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (Exception e) {
            // TODO: handle exception
            JOptionPane.showMessageDialog(this, "Đã có lỗi xảy ra, vui lòng thử lại sau....", "Oops",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
    }// GEN-LAST:event_btnInsertActionPerformed

